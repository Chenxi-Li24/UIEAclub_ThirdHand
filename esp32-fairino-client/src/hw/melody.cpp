const uint8_t MELODY_COUNT = sizeof(MELODY_LIST) / sizeof(MELODY_LIST[0]);

void melodyPlay(const MelodyDef *m) {
  if (!m)
    return;
  for (uint8_t i = 0; i < m->count; i++) {
    uint16_t freq = m->notes[i].freq;
    uint16_t dur = m->notes[i].dur;
    uint16_t gap = m->notes[i].gap;
    if (freq > 10000)
      freq = 10000;
    uint16_t total = dur + gap;
    if (total > 5000)
      total = 5000;
    hwBeep(freq, total);
  }
}
